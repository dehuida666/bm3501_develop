TONE_TASK_STATE TONE_TaskState;
uint8_t tone_PlayIndex = 0;


void Tone_PlayVoicePrompt(uint8_t tone_index)
{
	BT_SetVoicePromptByRawData();
	tone_PlayIndex = tone_index;
}

void Tone_PlayPowerOn(void)
{
	uint8_t power_on[BT_DSP_CODE_Power_On_LENGTH];
	
	memcpy(power_on,BT_DSP_Code_Power_On,BT_DSP_CODE_Power_On_LENGTH);
	BT_PlayVoicePromptByRawData(power_on,BT_DSP_CODE_Power_On_LENGTH);
}

void Tone_PlayBTPairing(void)
{
	uint8_t bt_paring[BT_DSP_CODE_BT_Pairing_LENGTH];
	
	memcpy(bt_paring,BT_DSP_Code_BT_Pairing,BT_DSP_CODE_BT_Pairing_LENGTH);
	BT_PlayVoicePromptByRawData(bt_paring,BT_DSP_CODE_BT_Pairing_LENGTH);
}

void BT_ToneTaskStart ( void )
{
    TONE_TaskState = TONE_TASK_START;
}


void BTMTONE_Task(void)
{
	switch(TONE_TaskState)
	{
		case TONE_TASK_START:
			if(tone_PlayIndex == TONE_PowerOn)
				TONE_TaskState = TONE_TASK_POWER_ON;
			else if(tone_PlayIndex == TONE_BTPairing)
				TONE_TaskState = TONE_TASK_BT_PAIRING;
			else
				TONE_TaskState = TONE_TASK_IDLE;
			break;

		case TONE_TASK_BT_PAIRING:
			Tone_PlayBTPairing();
			TONE_TaskState = TONE_TASK_IDLE;
			break;

		case TONE_TASK_POWER_ON:
			Tone_PlayPowerOn();
			TONE_TaskState = TONE_TASK_IDLE;
			break;

		default:
			break;

	}

}

void BTMTONE_EventHandler( BT_TTS_EVENTS event, uint8_t* paras, uint16_t size )
{
	switch(event)
	{
		case BT_EVENT_TTS_STATUS:
			if(paras[0] == 0x01)
			{
				User_Log("TTS status is ready\n");
				BT_ToneTaskStart();

			}
			break;

		default:
			break;

	}

}





